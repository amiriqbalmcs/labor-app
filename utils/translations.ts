export const useTranslation = (language: 'en' | 'ur' | 'hi' = 'en') => {
  const t = (key: string): string => {
    const keys = key.split('.');
    let value: any = translations[language];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || translations.en[key as keyof typeof translations.en] || key;
  };

  return { t };
};